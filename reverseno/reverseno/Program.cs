namespace reverseno;

class Program
{
    static void Main(string[] args)
    {
        Console.WriteLine("Original Number ");
        for (int i = 1; i <= 10; i++)
        {
            Console.WriteLine(i);
        }
        Console.WriteLine("Reverse Number ");
        for (int j = 10; j > 0; j--)
        {
            Console.WriteLine(j);
        }
        Console.ReadLine();
    }
}

